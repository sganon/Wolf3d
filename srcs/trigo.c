#include "wolf.h"

double		get_cos(double a)
{
	a = ((a / 100.0) * 2.0 * M_PI) / 360.0;
	return (cos(a));
}

double		get_sin(double a)
{
	a = ((a / 100.0) * 2.0 * M_PI) / 360.0;
	return (sin(a));
}

double		get_tan(double a)
{
	a = ((a / 100.0) * 2.0 * M_PI) / 360.0;
	return (tan(a));
}
